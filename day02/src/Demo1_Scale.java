public class Demo1_Scale {
    public static void main(String[] args) {
        /*
         * 0b100
         * 0100
         * 100
         * 0x100
         */
        System.out.println(0b100);          // 4
        System.out.println(0100);           // 64
        System.out.println(100);            // 100
        System.out.println(0x100);          // 256
    }
}
