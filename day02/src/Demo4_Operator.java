public class Demo4_Operator {
    public static void main(String[] args) {
        // 关系运算符
        // ==, !=, >, >=, <, <=
        System.out.println(4 == 3);
        System.out.println(4 != 3);
        System.out.println(4 > 3);
        System.out.println(4 >= 3);
        System.out.println(4 > 4);
        System.out.println(4 < 3);
        System.out.println(4 <= 3);

    }
}
